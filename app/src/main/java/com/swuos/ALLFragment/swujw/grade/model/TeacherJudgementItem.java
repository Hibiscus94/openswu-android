public class TeacherJudgementItem {
    String kch_id;
    String jgh_id;
    String xsdm;
    String jxb_id;
    String teacherName;
    String className;

    public String getKch_id() {
        return kch_id;
    }

    public void setKch_id(String kch_id) {
        this.kch_id = kch_id;
    }

    public String getJgh_id() {
        return jgh_id;
    }

    public void setJgh_id(String jgh_id) {
        this.jgh_id = jgh_id;
    }

    public String getXsdm() {
        return xsdm;
    }

    public void setXsdm(String xsdm) {
        this.xsdm = xsdm;
    }

    public String getJxb_id() {
        return jxb_id;
    }

    public void setJxb_id(String jxb_id) {
        this.jxb_id = jxb_id;
    }

    public String getTeacherName() {
        return teacherName;
    }

    public void setTeacherName(String teacherName) {
        this.teacherName = teacherName;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }
}
